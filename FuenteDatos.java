    public FuenteDatos() {
        initComponents();
        int cantFuentes = 0;
        int tamTrama = 1;
        while(tamTrama >= cantFuentes){
            System.out.println("Tamanio trama debe ser menor que cantidad fuentes");
            cantFuentes = Integer.parseInt(JOptionPane.showInputDialog(null, "Cantidad de fuentes?"));
            tamTrama = Integer.parseInt(JOptionPane.showInputDialog(null, "Tamanio trama?"));
        }//while

        m = new Multiplex(cantFuentes, tamTrama);
        numFuentes = cantFuentes;
        ttrama = tamTrama;
    }//constructor

