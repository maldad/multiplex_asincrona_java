    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int fuente;
        int datos;
        try{
            fuente = Integer.parseInt(jTextField1.getText());
            datos = Integer.parseInt(jTextField2.getText());
        }catch(NumberFormatException nfe){
            System.out.println("Falta algun dato");
            return;
        }

        if(fuente > numFuentes){
            System.out.println("No existe tal numero de fuente");
            return;
        }
        m.addData(fuente-1, datos);
        jTextField1.setText("");
        jTextField2.setText("");
        m.nombres();
        System.out.println();
    }//GEN-LAST:event_jButton1ActionPerformed

